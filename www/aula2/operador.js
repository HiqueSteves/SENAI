console.log(5 == 5) // 5 é igual a 5
console.log(5 != 5) // 5 é igual a 5, e não diferentes, sendo assim falso
console.log(10 > 7) // 10 é maior que 7
console.log(3 < 5) // 3 é menor que 5
console.log(7 >= 7) // 7 é menor e igual a 7
console.log(4 <= 9) // 4 é menor que 9
console.log(5 == 5 && 10 > 7) // 5 é igual a 5 e 10 é maior que 7
console.log(5 == 4 && 10 > 7) //as 2 tem q ser verdadeiras, mas como 4 nao é igual a 5, o codigo todo vira falso 
console.log(5 == 5 || 5 > 7) // como a alternativa é ou um ou outro, mesmo 5 > 7 sendo falso, a alternativa é verdadeira
console.log(5 == 4 || 5 > 7) // as duas são falsas